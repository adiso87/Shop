package com.jelastic.adiso87.Shop;

public class NumberClass {

	private int first;
	private int last;
	
	public int getSum(){
		return first + last;
	}
	public int getFirst() {
		return first;
	}
	public void setFirst(int first) {
		this.first = first;
	}
	public int getLast() {
		return last;
	}
	public void setLast(int last) {
		this.last = last;
	}
}
